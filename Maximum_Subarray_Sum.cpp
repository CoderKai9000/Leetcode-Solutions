class Solution {
public:
    int maxSubArray(vector<int>& nums) {
        int start = 0, end = 0, max = nums[0], sum = nums[0];
        while(end < nums.size()) {
            if(sum < 0) {
                end++;
                if(end == nums.size()) break;
                start = end;
                sum = nums[start];
            } else {
                end++;
                if(end == nums.size()) break;
                sum += nums[end];
            }
            if(sum > max) max = sum;
        }
        return max;
    }
};

int main() {
    Solution s;
    vector<int> nums = {-2,1,-3,4,-1,2,1,-5,4};
    cout << s.maxSubArray(nums) << endl;
    return 0;
}